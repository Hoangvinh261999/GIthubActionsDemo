package com.poly.entity;

public class videoGD1 {

	String name;
	String img;
	String title;
	
	


	public videoGD1(String name, String img, String title) {
		
		this.name = name;
		this.img = img;
		this.title = title;
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	public String getImg() {
		return img;
	}


	public void setImg(String img) {
		this.img = img;
	}


	public String getTitle() {
		return title;
	}


	public void setTitle(String title) {
		this.title = title;
	}
	
}
